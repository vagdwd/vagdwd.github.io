var datamap = {};
db.transaction(function(tx) {
    // here be the transaction
    // do SQL magic here using the tx object
    var query = 'SELECT url,x,y,r FROM clickdata';
    tx.executeSql(query, [], function(tx, results) {
        var len = results.rows.length,
            i;
        for (i = 0; i < len; i++) {
            data.clickdata.push(results.rows.item(i));
        }
        handleClickDataLoaded();
    });
});

function handleClickDataLoaded() {
    for (var i = 0; i < data.clickdata.length; i++) {
        var key = data.clickdata[i].x + 'x' + data.clickdata[i].y;
        if (datamap.hasOwnProperty(key)) {
            datamap[key]++;
        } else {
            datamap[key] = 1;
        }
    };
    var keys = Object.keys(datamap);
    var maxcount = 0;
    for (var i = 0; i < keys.length; i++) {
        var x = keys[i].split('x')[0],
            y = keys[i].split('x')[1],
            count = datamap[keys[i]];
        if (count > maxcount) {
            maxcount = count;
        }
        data.data.push({
            x: parseInt(x),
            y: parseInt(y),
            count: count
        });
        data.max = maxcount;
    };
}

